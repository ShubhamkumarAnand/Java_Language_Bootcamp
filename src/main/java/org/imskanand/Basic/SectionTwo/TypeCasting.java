package org.imskanand.Basic.SectionTwo;

public class TypeCasting {
    public static void main(String[] args) {

        // float to int conversions
        float myAge = 21.9f;
        int myAgeInt = (int)myAge;
        System.out.println("My Age : "+ myAge);
        System.out.println("My Age : "+ myAgeInt);

        // int to float conversions
        int mySalary = 3244;
        float mySalaryFloat = (float)mySalary;
        System.out.println("My Salary : "+ mySalary);
        System.out.println("My Salary : "+ mySalaryFloat);
    }
}
